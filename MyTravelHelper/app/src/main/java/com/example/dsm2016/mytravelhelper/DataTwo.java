package com.example.dsm2016.mytravelhelper;

public class DataTwo {

    private String name;
    private String user;

    public DataTwo(){}
    public DataTwo(String name, String user){
        this.name = name;
        this.user = user;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }
}
